void secuencia(int new_state){
  switch (new_state)
  {
  case ST_wind:

    ADC_Select_CH0();
    HAL_ADC_Start(&hadc1);
    HAL_ADC_PollForConversion(&hadc1,1000);
    viento = HAL_ADC_GetValue(&hadc1);
    printf("Sensor Viento: %d\r\n",viento);
    htim2.Instance->CCR1 = 25 + (viento*475/MAX_ANGLE);
    viento = (viento * 400) / 4095; //400 son los km/h máximos
    HAL_TIM_PWM_Start(&htim2,TIM_CHANNEL_1);
    HAL_TIM_IC_Start_IT(&htim3,TIM_CHANNEL_1);
    printf("milisegundos: %d\r\n",usWidth/1000);
    HAL_ADC_Stop(&hadc1);
    
    next_state = ST_temp;

    break;
  
  case ST_temp:

    ADC_Select_CH1();
    HAL_ADC_Start(&hadc1);
    HAL_ADC_PollForConversion(&hadc1,1000);
    int val2 = HAL_ADC_GetValue(&hadc1);
    HAL_ADC_Stop(&hadc1);
    temperatura = (3.3 * val2 * 10)/4095;
    printf("Sensor temperatura: %d\r\n", temperatura);
    
    next_state = ST_light;
    
    break;

  case ST_light:

    ADC_Select_CH4();
    HAL_ADC_Start(&hadc1);
	  HAL_ADC_PollForConversion(&hadc1, 1000);
    int val3 = HAL_ADC_GetValue(&hadc1);
    luminosidad = val3 * 100 / 4095;
    HAL_ADC_Stop(&hadc1);
    printf("Sensor luminosidad%%: %d\r\n", luminosidad);
    next_state = ST_sound;

    break;
  
  case ST_sound:

    ADC_Select_CH8();
    HAL_ADC_Start(&hadc1);
	  HAL_ADC_PollForConversion(&hadc1, 1000);
    int val4 = HAL_ADC_GetValue(&hadc1);
    for (int i = 0; i < 4095; i++)
    {
      val4 += HAL_ADC_GetValue(&hadc1);
    }
    val4 = val4 / 4095;
    sonido = (val4+83)/11;
    HAL_ADC_Stop(&hadc1);
    printf("Sensor sonido: %d\r\n", sonido);
    next_state = ST_send;

    break;
  
  case ST_send:

    make_message();
    next_state = ST_wait;
    break;
  
  case ST_wait:
    lluvia = 0;
    printf("-------------------------------------------------------\r\n");
    next_state = ST_wind;
    HAL_Delay(5000);
    break;
  }
}

